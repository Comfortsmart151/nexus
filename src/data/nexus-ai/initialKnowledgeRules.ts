export function findInitialNexusAiKnowledgeRuleById(
  ruleId: string,
): NexusAiKnowledgeRule | null {
  return (
    INITIAL_NEXUS_AI_KNOWLEDGE_RULES.find(
      (rule) => rule.id === ruleId,
    ) ?? null
  );
}

export function findInitialNexusAiKnowledgeRuleByCode(
  code: string,
): NexusAiKnowledgeRule | null {
  const normalizedCode = code.trim().toLowerCase();

  return (
    INITIAL_NEXUS_AI_KNOWLEDGE_RULES.find(
      (rule) =>
        rule.code.trim().toLowerCase() === normalizedCode,
    ) ?? null
  );
}

export function findInitialNexusAiKnowledgeRulesByCategory(
  category: NexusAiKnowledgeRule["category"],
): NexusAiKnowledgeRule[] {
  return INITIAL_NEXUS_AI_KNOWLEDGE_RULES.filter(
    (rule) =>
      rule.isActive && rule.category === category,
  );
}

export function getActiveInitialNexusAiKnowledgeRules(): NexusAiKnowledgeRule[] {
  return INITIAL_NEXUS_AI_KNOWLEDGE_RULES.filter(
    (rule) => rule.isActive,
  );
}